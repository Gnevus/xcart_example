namespace XLite\Module\XCExample\Recommendations\View;

/**
 * Recommendation modify page view
 *
 * @ListChild (list="admin.center", zone="admin")
 */
class Recommendation extends \XLite\View\AView
{
    /**
     * Return list of allowed targets
     *
     * @return array
     */
    public static function getAllowedTargets()
    {
        return array_merge(parent::getAllowedTargets(), ['recommendation']);
    }

    /**
     * Register CSS files
     *
     * @return array
     */
    public function getCSSFiles()
    {
        $list = parent::getCSSFiles();

        $list[] = 'modules/XCExample/Recommendations/recommendation/style.css';

        return $list;
    }

    /**
     * Return widget default template
     *
     * @return string
     */
    protected function getDefaultTemplate()
    {
        return 'modules/XCExample/Recommendations/recommendation/body.twig';
    }
}
